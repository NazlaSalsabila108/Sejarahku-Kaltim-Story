                        <h3>Patung Lembuswana</h3>
                        <p>
                            Patung Lembuswana adalah simbol mitologi Kutai yang melambangkan kekuatan dan semangat kepemimpinan yang mulia, dipercaya sebagai kendaraan spiritual Raja Mulawarman dari Kerajaan Kutai Kuno dan penjaga Sungai Mahakam.
                            Patung ini berwujud hewan perpaduan berbagai makhluk, seperti berkepala singa, bermahkota, berbelalai gajah (simbol kecerdasan), bersayap garuda (simbol keperkasaan), bersisik ikan, serta bertaji, dan sering dikaitkan dengan kelahiran Putri Karang Melenu. 
                            Patung yang menjadi ciri khas ini, sebenarnya adalah karya seniman Burma pada abad ke-19, tapi baru menghiasi pelataran kedaton Kutai Kartanegara sejak awal abad ke-20.
