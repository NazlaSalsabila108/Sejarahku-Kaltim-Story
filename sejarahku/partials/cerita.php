                        <h3>Kering Bukit Kang</h3>
                        <p>
                            Kering Bukit Kang adalah sebuah keris yang digunakan oleh Permaisuri Aji Putri Karang Melenu, permaisuri Raja Kutai Kartanegra yang pertama. 
                            Berdasarkan legenda sejarah, permaisuri ini adalah putri yang ditemukan dalam sebuah gong yang hanyut di atas balai bambu. Dalam gong tersebut, selain ada seorang bayi perempuan, di dalamnya juga terdapat telur ayam dan sebuah keris, yaitu keris bukit kang.
                            Keris ini menjadi peninggalan bersejarah yang menunjukkan hubungan antara kerajaan Kutai dan kerajaan Cina.
                        </p>
                    
