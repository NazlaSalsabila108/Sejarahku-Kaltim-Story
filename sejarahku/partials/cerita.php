                            Pada 1936, Sultan Aji Muhammad Parikesit memerintahkan pembangunan istana baru di Tenggarong yang terbuat dari beton, menggantikan istana lama yang terbuat dari kayu. 
                            Pembangunan ini didanai oleh kekayaan hasil tambang batu bara dan minyak bumi, dan selesai pada 1937.
                            Setelah Kesultanan Kutai digabungkan ke Indonesia pada 1960, istana ini diserahkan kepada pemerintah. Pada 1976, istana diresmikan menjadi Museum Mulawarman, yang menyimpan berbagai peninggalan bersejarah Kesultanan Kutai. 
                            Nama Mulawarman diambil dari nama raja Kerajaan Kutai Martadipura yang terkenal akan kebijaksanaannya.
